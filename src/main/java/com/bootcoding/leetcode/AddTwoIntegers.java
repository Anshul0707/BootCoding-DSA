package com.bootcoding.leetcode;

public class AddTwoIntegers
{
    public static void main(String[] args) {
        int num1=10;
        int num2=5;
        int sum = sum(num1, num2);
        System.out.println(sum);
    }

    private static int sum(int num1, int num2) {
        return num1+num2;
    }
}
